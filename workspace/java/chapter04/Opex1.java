package chapter04;

public class Opex1 {
	public static void main(String[] args) {
	
		int number1 = 10;
		System.out.println("number1  = 10 -> "+number1);
		
		number1 += 10;		// 더하기
		System.out.println("number1 += 10 -> "+number1);
		
		number1 -= 10;		// 빼기 
		System.out.println("number1 -= 10 -> "+number1);
		
		number1 *= 2;		// 곱하기
		System.out.println("number1 *= 2 -> "+number1);
		
		number1 /= 2;		// 나누기
		System.out.println("number1 /= 2 -> "+number1);
		
		number1 %= 3;		// 나머지
		System.out.println("number1 %= 2 -> "+number1);
		
		
	}

}
